<?php

class ApiModel
{

    public  function suma($value1, $value2)
    {
        return $value1 + $value2;
    }

    public  function resta($value1, $value2)
    {
        return $value1 - $value2;
    }

    public  function multiplicacion($value1, $value2)
    {
        return $value1 * $value2;
    }

    public  function division($value1, $value2)
    {
        return $value1 / $value2;
    }
}
